　习近平：坚决打击暴恐活动 维护国家安全稳定 
<p>　　央广网北京4月9日消息（记者陈欣）中共中央总书记、国家主席、中央军委主席习近平9日视察了中国人民武装警察部队特种警察学院并为“猎鹰突击队”授旗，代表党中央、国务院、中央军委向大家并向武警部队全体官兵致以诚挚的问候,强调武警部队作为国家反恐维稳的重要力量，要认真贯彻党中央决策部署，坚决有力打击各种暴力恐怖犯罪活动，维护国家安全和社会稳定，保障人民安居乐业。 
</p><p>　　上午9时30分许，习近平来到位于北京市郊的武警特种警察学院。在了解学院全面建设情况后，习近平来到训练中心大楼二层，观看反恐课目训练演示。训练场上，女子特战队员依次采用微型冲锋枪、手枪对人形靶实施快速精准射击，发发命中目标。男子特战队员演示的特战狙击，展示了贴近实战、一击必杀的高超本领。特战小组搜剿行动演示中，快速反应、密切协同，超越射击、水力破门、合围攻击、乘车追击，发起一个个凌厉攻势。在训练中心大楼二层搏击馆和三层搏击专修馆，习近平观看了特战队员匕首格斗术、摔擒制敌术、极限搏击和散打实战对抗训练。反恐战斗实验馆一层，正在进行紧张激烈的反劫机舱门开启训练。特战队员拉开舱门，突入机舱，动作干净利落。习近平同参训特战队员一一握手、亲切交谈，详细询问教学训练和生活情况，对大家的精湛技能和高昂士气给予高度赞扬。习近平语重心长地勉励大家，特战队员战斗在反恐第一线，时刻面临生与死、血与火的考验。要坚持从难从严训练，保持高度戒备，真正成为特战精英、反恐尖兵。 
</p><p>　　11时许，习近平来到训练中心观礼台前，出席“猎鹰突击队”授旗仪式。在嘹亮的国歌声中，特战官兵按战斗着装整齐列队，武警部队领导宣读《中国人民武装警察部队关于“猎鹰突击队”命名的通知》。习近平将队旗郑重授予“猎鹰突击队”大队长，全体队员向队旗行举手礼并庄严宣誓。 
</p><p>　　随后，习近平发表了重要讲话。他表示，看了你们的反恐训练，看到大家训练有素、作风顽强，我感到十分高兴。习近平强调，“猎鹰突击队”是国家级反恐拳头部队。希望大家牢记强军目标，严格训练、严格要求、严格管理，做到思想政治非常过硬、专业素质非常过硬、战斗作风非常过硬，努力成为国际一流水平的反恐特战劲旅，永远做党和人民的忠诚卫士。 
</p><p>　　武警特种警察学院原是1982年成立的反劫机特种警察部队，是武警总部直接领导和指挥的一支国家反恐力量。学院组建以来，先后为国家和军队输送了一大批特战人才，出色完成一系列安保、处突特殊任务，为维护社会稳定作出突出贡献。2014年2月20日，习近平主席命名学院特种作战大队为中国人民武装警察部队“猎鹰突击队”。 
</p><p>　　范长龙、许其亮、孟建柱、栗战书、郭声琨和李家祥、王建平、许耀元等参加有关活动。</p></div>
